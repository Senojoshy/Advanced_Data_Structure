#include <stdio.h>

#define SIZE 5

int stack[SIZE];
int top = -1;   


void push()
{
    int value;
    if (top == SIZE - 1)
    {
        printf("\nStack Overflow! Cannot push.\n");
    }
    else
    {
        printf("Enter value to push: ");
        scanf("%d", &value);
        top++;
        stack[top] = value;
        printf("Pushed %d\n", value);
    }
}

void pop()
{
    if (top == -1)
    {
        printf("\nStack Underflow! Cannot pop.\n");
    }
    else
    {
        printf("Popped element = %d\n", stack[top]);
        top--;
    }
}

void peek()
{
    if (top == -1)
    {
        printf("\nStack is empty.\n");
    }
    else
    {
        printf("\nTop element = %d\n", stack[top]);
    }
}

void display()
{
    int i;
    if (top == -1)
    {
        printf("\nStack is empty.\n");
    }
    else
    {
        printf("\nStack elements:\n");
        for (i = top; i >= 0; i--)
        {
            printf("%d\n", stack[i]);
        }
    }
}


int main()
{
    int choice;

    while (1)
    {
        printf("\n--- STACK MENU ---\n");
        printf("1. Push\n");
        printf("2. Pop\n");
        printf("3. Peek\n");
        printf("4. Display\n");
        printf("5. Exit\n");
        printf("Enter your choice: ");
        scanf("%d", &choice);

        switch (choice)
        {
            case 1: push();    break;
            case 2: pop();     break;
            case 3: peek();    break;
            case 4: display(); break;
            case 5: return 0;
            default: printf("\nInvalid Choice!\n");
        }
    }

    return 0;
}
